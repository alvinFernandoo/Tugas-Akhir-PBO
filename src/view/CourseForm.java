    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete this Course?",
            "Delete Confirmation",
            JOptionPane.YES_NO_OPTION
        );

        if(confirm == JOptionPane.YES_OPTION) {
            // Proses delete
            try {
                controller.delete(selectedKode);
                JOptionPane.showMessageDialog(this,"Course deleted successfully!");
                this.selectedKode=null;
                this.clearForm();
                loadCourseData();
            } catch (Exception ex) {
                Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"deleted unsuccessfully!");
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldNama.getText();
        String kode = jTextFieldKode.getText();
        String strSKS = jComboBoxSKS.getSelectedItem().toString();
        String strSemester = jComboBoxSemester.getSelectedItem().toString();

        if(nama.isEmpty() || kode.isEmpty() || strSKS.isEmpty() || strSemester.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill in all fields ", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
        else {

            int SKS = Integer.parseInt(strSKS);
            int semester = Integer.parseInt(strSemester);
            Course course = new Course(kode, nama, SKS, semester);
            try {

                int res;
                if(selectedKode != null){

                    res = controller.update(course, selectedKode);
                }
                else{
                    res = controller.create(course);
                }
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "Data created successfully ", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.selectedKode=null;
                    this.clearForm();
                    loadCourseData();
                }
                else {
                    JOptionPane.showMessageDialog(this, "Error occurred while inserting ", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButtonSimpanActionPerformed

