    private void jTableMatkulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMatkulMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTableMatkul.getSelectedRow();
        if (selectedRow != -1) {
            jTextFieldNama.setText(jTableMatkul.getValueAt(selectedRow, 0).toString());
            jTextFieldKode.setText(jTableMatkul.getValueAt(selectedRow, 1).toString());
            jTextFieldKode.setEditable(false);
            selectedKode = jTableMatkul.getValueAt(selectedRow, 1).toString();

            String sksVal = jTableMatkul.getValueAt(selectedRow, 2).toString();
            jComboBoxSKS.setSelectedItem(sksVal);

            String semVal = jTableMatkul.getValueAt(selectedRow, 3).toString();
            jComboBoxSemester.setSelectedItem(semVal);
        }
    }//GEN-LAST:event_jTableMatkulMouseClicked

    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String keyword = jTextFieldSearch.getText().toLowerCase();
        List<Course> all = controller.getCourse();
        List<Course> filtered = new java.util.ArrayList<>();
    
        for (Course l : all) {
            if (l.getCourseName().toLowerCase().contains(keyword) ||
                l.getCode().toLowerCase().contains(keyword) ||
                l.getSKSString().toLowerCase().contains(keyword) ||
                l.getSemesterString().toLowerCase().contains(keyword)) {  // ← tambahkan ini
                filtered.add(l);
                }
        }
    
        String[][] data = new String[filtered.size()][4];
        for (int i = 0; i < filtered.size(); i++) {
            data[i][0] = filtered.get(i).getCode();
            data[i][1] = filtered.get(i).getCourseName();
            data[i][2] = filtered.get(i).getSKSString();
            data[i][3] = filtered.get(i).getSemesterString();
            }
        String[] cols = {"Kode", "Nama", "SKS", "Semester"};
        jTableMatkul.setModel(new javax.swing.table.DefaultTableModel(data, cols));
    }//GEN-LAST:event_jButtonSearchActionPerformed

